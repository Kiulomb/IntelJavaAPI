package lib;

import org.json.JSONObject;

public class IngressPortal {

	private String id;
	private String name;
	private double latitude, longitude;

	public IngressPortal(String id, String name, double latitude, double longitude) {
		this.id = id;
		this.name = name;
		this.latitude = latitude;
		this.longitude = longitude;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	@Override
	public String toString() {
		return "IngressPortal: id=" + id + ", name=" + name + ", latitude=" + latitude + ", longitude=" + longitude;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (!(obj instanceof IngressPortal))
			return false;
		IngressPortal other = (IngressPortal) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	public JSONObject toJSON() {
		JSONObject json = new JSONObject();
		json.put("n", name);
		json.put("la", latitude);
		json.put("lo", longitude);

		return json;
	}
}
